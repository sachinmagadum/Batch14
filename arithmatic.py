def addition(no1, no2):
    ans = no1 + no2
    return ans


number1 = 10
number2 = 20
ans = addition(number1, number2)
print(f"Addition of {number1} and {number2} is {ans}")


def multiplication(no1, no2):
    ans = no1 * no2
    return ans


number1 = 10
number2 = 20
ans = multiplication(number1, number2)
print(f"Multiplication of {number1} and {number2} is {ans}")

def Division(no1, no2):
    ans = no1 / no2
    return ans


number1 = 20
number2 = 10
ans = Division(number1, number2)
print(f"Division of {number1} and {number2} is {ans}")